/**Math Rounding:**
Declare a variable `number = 56.78`. Use `Math.ceil`, `Math.floor`, and `Math.round` to round the number in different ways.
 Print the results.*/
 let number = 56.78
 let higherRound = Math.ceil(number)
 console.log(higherRound)
 let lowerRound = Math.floor(number)
 console.log(lowerRound)
 let noRound = Math.round(number)
 console.log(noRound)
 //code will print 57 , 56 , 57 respectively
 
 
 